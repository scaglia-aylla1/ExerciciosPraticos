
namespace ExerciciosPraticos;
/*Crie um programa que concatene um nome e um sobrenome inseridos pelo usuário e 
 * ao final exiba o nome completo.
 */
public class NomeCompleto
{
    public static void Nome(string nome, String sobrenome)
    {
        Console.WriteLine($"Nome completo: {nome} {sobrenome}");
    }
}
